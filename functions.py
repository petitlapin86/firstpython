#helpful functions
#these are built in functions that you can use in python with some example of how to use them

#instance
isinstance('a', str) #is instance 'a' a string?

isinstance(5.2, (int, float)) #is instance 5.2 a int or a float?

#subclass
issubclass(Thief, Character) #tells you whether its a subclass of another class - so here im asking is theif a subclass of character?

#type function
type() #tells you the type of object that an instance is
